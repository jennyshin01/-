public class 화면글자출력 {
    //예제부터 시작
    public static void main(String args[]){
    //print()와 println()의 차이!
        System.out.println("Hello, world");
        //화면에 hello world를 적고 줄바꿈을 한다.
        System.out.print("Hello");
        //화면에 hello world를 적고 줄바꿈을 하지 않는다.
        System.out.println("World");
    }
}
